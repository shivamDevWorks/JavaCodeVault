package graphPractice.utitlity;

public class Pair {

    private int val1,val2;

    public Pair(int val1,int val2){
        this.val1=val1;
        this.val2=val2;
    }

    public int getVal1(){
        return this.val1;
    }

    public int getVal2(){
        return this.val2;
    }

    public void setVal1(int val1){
        this.val1=val1;
    }

    public void setVal2(int val2){
        this.val2=val2;
    }
}
